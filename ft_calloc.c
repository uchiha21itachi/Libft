#include "libft.h"

void	*ft_calloc(size_t count, size_t size)
{
	void	*p;
	size_t	total;

	total = count * size;
	p = malloc(total);
	if (p == 0)
		return (NULL);
	else
		ft_bzero(p, total);
	return (p);
}
